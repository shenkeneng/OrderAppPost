package com.frxs.order.model;

import com.google.gson.annotations.SerializedName;

import java.io.Serializable;

/**
 * Created by ewu on 2016/5/6.
 */
public class AttributePicture implements Serializable {

    @SerializedName("ImageUrlOrg")
    private String ImageUrlOrg;

    @SerializedName("ImageUrl200x200")
    private String ImageUrl200x200;

    public String getImageUrlOrg() {
        return ImageUrlOrg;
    }

    public void setImageUrlOrg(String imageUrlOrg) {
        ImageUrlOrg = imageUrlOrg;
    }

    public String getImageUrl200x200() {
        return ImageUrl200x200;
    }

    public void setImageUrl200x200(String imageUrl200x200) {
        ImageUrl200x200 = imageUrl200x200;
    }
}
