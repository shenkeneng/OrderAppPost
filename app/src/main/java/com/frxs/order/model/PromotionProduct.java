package com.frxs.order.model;

import java.io.Serializable;

/**
 * 促销活动商品 By Tiepier
 */

public class PromotionProduct implements Serializable {
    // 商品编码
    private int ProductId;
    // 商品名称
    private String ProductName;
    // 商品副标题
    private String ProductName2;
    // SKU
    private String SKU;
    // 仓库商品状态(0:已移除1:正常;2:淘汰;3:冻结;)
    private int WStatus;
    // 配送价
    private double SalePrice;
    // 库存单位
    private String Unit;
    // 配送(销售)单位
    private String SaleUnit;
    // 包装数
    private double PackingQty;
    // 图片
    private String ImageUrl200x200;
    // 配送积分兑物总价（销售配送价 * 包装数）
    private double BigShopPoint;
    //赠品 搭售 标识符
    private int IsGift;
    //当前商品在购物车中的数量
    private int CartQty;
    //平台费率
    private double ShopAddPerc;
    //最小起定量
    private Double MinPreQty;
    //最大起定量
    private Double MaxaPreQty;
    //赠品 搭售数量
    private Double GiftsQty;

    public Double getGiftsQty() {
        return GiftsQty;
    }

    public void setGiftsQty(Double giftsQty) {
        GiftsQty = giftsQty;
    }

    public Double getMinPreQty() {
        return MinPreQty;
    }

    public void setMinPreQty(Double minPreQty) {
        MinPreQty = minPreQty;
    }

    public Double getMaxaPreQty() {
        return MaxaPreQty;
    }

    public void setMaxaPreQty(Double maxaPreQty) {
        MaxaPreQty = maxaPreQty;
    }

    public double getShopAddPerc() {
        return ShopAddPerc;
    }

    public void setShopAddPerc(double shopAddPerc) {
        ShopAddPerc = shopAddPerc;
    }

    public int getIsGift() {
        return IsGift;
    }

    public void setIsGift(int isGift) {
        IsGift = isGift;
    }

    public double getBigShopPoint() {
        return BigShopPoint;
    }

    public void setBigShopPoint(double bigShopPoint) {
        BigShopPoint = bigShopPoint;
    }

    public int getProductId() {
        return ProductId;
    }

    public void setProductId(int productId) {
        ProductId = productId;
    }

    public String getProductName() {
        return ProductName;
    }

    public void setProductName(String productName) {
        ProductName = productName;
    }

    public String getProductName2() {
        return ProductName2;
    }

    public void setProductName2(String productName2) {
        ProductName2 = productName2;
    }

    public String getSKU() {
        return SKU;
    }

    public void setSKU(String SKU) {
        this.SKU = SKU;
    }

    public int getWStatus() {
        return WStatus;
    }

    public void setWStatus(int WStatus) {
        this.WStatus = WStatus;
    }

    public double getSalePrice() {
        return SalePrice;
    }

    public void setSalePrice(double salePrice) {
        SalePrice = salePrice;
    }

    public String getUnit() {
        return Unit;
    }

    public void setUnit(String unit) {
        Unit = unit;
    }

    public String getSaleUnit() {
        return SaleUnit;
    }

    public void setSaleUnit(String saleUnit) {
        SaleUnit = saleUnit;
    }

    public double getPackingQty() {
        return PackingQty;
    }

    public void setPackingQty(double packingQty) {
        PackingQty = packingQty;
    }

    public String getImageUrl200x200() {
        return ImageUrl200x200;
    }

    public void setImageUrl200x200(String imageUrl200x200) {
        ImageUrl200x200 = imageUrl200x200;
    }

    public int getCartQty() {
        return CartQty;
    }

    public void setCartQty(int cartQty) {
        CartQty = cartQty;
    }
}
