package com.frxs.order.model;

import java.util.List;

/**
 * Created by Chentie on 2017/6/27.
 */

public class PrefectShopInfo {

    private String ShopCode;
    private int ShopID;
    private String ShopName;
    private int ShopType;// 门店类型 0：加盟店  1：签约店
    private int Status;// 门店状态 0：状态   1：正常
    private List<String> ShopErrInfo;

    public String getShopCode() {
        return ShopCode;
    }

    public void setShopCode(String ShopCode) {
        this.ShopCode = ShopCode;
    }

    public int getShopID() {
        return ShopID;
    }

    public void setShopID(int ShopID) {
        this.ShopID = ShopID;
    }

    public String getShopName() {
        return ShopName;
    }

    public void setShopName(String ShopName) {
        this.ShopName = ShopName;
    }

    public int getShopType() {
        return ShopType;
    }

    public void setShopType(int ShopType) {
        this.ShopType = ShopType;
    }

    public int getStatus() {
        return Status;
    }

    public void setStatus(int Status) {
        this.Status = Status;
    }

    public List<String> getShopErrInfo() {
        return ShopErrInfo;
    }

    public void setShopErrInfo(List<String> ShopErrInfo) {
        this.ShopErrInfo = ShopErrInfo;
    }
}
