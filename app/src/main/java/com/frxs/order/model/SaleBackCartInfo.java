package com.frxs.order.model;

import java.util.List;

/**
 * Created by shenpei on 2017/6/9.
 * 退货车商品
 */

public class SaleBackCartInfo {

    private String ID; //新增商品时可不传

    private String WID;

    private String ShopID;

    private String ProductID; // 商品ID

    private double Qty; //商品数量

    private String BuyOrderTime; //门店商品最后一次购买时间(YYYY-MM-dd HH:mm:ss)

    private int BackReasonCode; //01:商品破损; 02: 做工瑕疵问题; 03:少发/漏发; 99:其它；

    private String BackReasonName;//01:商品破损; 02: 做工瑕疵问题; 03:少发/漏发; 99:其它；

    private String BackReasonDes;//退货原因问题描述

    private List<UrlList> UrlList;

    public String getID() {
        return ID;
    }

    public void setID(String ID) {
        this.ID = ID;
    }

    public String getWID() {
        return WID;
    }

    public void setWID(String WID) {
        this.WID = WID;
    }

    public String getShopID() {
        return ShopID;
    }

    public void setShopID(String shopID) {
        ShopID = shopID;
    }

    public String getProductID() {
        return ProductID;
    }

    public void setProductID(String productID) {
        ProductID = productID;
    }

    public double getQty() {
        return Qty;
    }

    public void setQty(double qty) {
        Qty = qty;
    }

    public String getBuyOrderTime() {
        return BuyOrderTime;
    }

    public void setBuyOrderTime(String buyOrderTime) {
        BuyOrderTime = buyOrderTime;
    }

    public int getBackReasonCode() {
        return BackReasonCode;
    }

    public void setBackReasonCode(int backReasonCode) {
        BackReasonCode = backReasonCode;
    }

    public String getBackReasonName() {
        return BackReasonName;
    }

    public void setBackReasonName(String backReasonName) {
        BackReasonName = backReasonName;
    }

    public String getBackReasonDes() {
        return BackReasonDes;
    }

    public void setBackReasonDes(String backReasonDes) {
        BackReasonDes = backReasonDes;
    }

    public List<UrlList> getUrlList() {
        return UrlList;
    }

    public void setUrlList(List<UrlList> urlList) {
        UrlList = urlList;
    }
}
