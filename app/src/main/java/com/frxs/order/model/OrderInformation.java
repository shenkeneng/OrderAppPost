package com.frxs.order.model;

import java.io.Serializable;

/**
 * 订单信息实体 By Tiepier
 */
public class OrderInformation implements Serializable {


    private static final long serialVersionUID = -6899489834719578343L;

    //订单信息
    private int OrderID;
    private String OrderTime;
    private int Order;
    private int OrderCount;
    private double OrderAmount;
    private double StoreIntegral;

    //装箱信息
    private int PickingTurnover;
    private int PickingBox;
    private int PickingFragile;
    private String PickingRemark;

    public int getOrderID() {
        return OrderID;
    }

    public void setOrderID(int orderID) {
        OrderID = orderID;
    }

    public String getOrderTime() {
        return OrderTime;
    }

    public void setOrderTime(String orderTime) {
        OrderTime = orderTime;
    }

    public int getOrder() {
        return Order;
    }

    public void setOrder(int order) {
        Order = order;
    }

    public int getOrderCount() {
        return OrderCount;
    }

    public void setOrderCount(int orderCount) {
        OrderCount = orderCount;
    }

    public double getOrderAmount() {
        return OrderAmount;
    }

    public void setOrderAmount(double orderAmount) {
        OrderAmount = orderAmount;
    }

    public double getStoreIntegral() {
        return StoreIntegral;
    }

    public void setStoreIntegral(double storeIntegral) {
        StoreIntegral = storeIntegral;
    }

    public int getPickingTurnover() {
        return PickingTurnover;
    }

    public void setPickingTurnover(int pickingTurnover) {
        PickingTurnover = pickingTurnover;
    }

    public int getPickingBox() {
        return PickingBox;
    }

    public void setPickingBox(int pickingBox) {
        PickingBox = pickingBox;
    }

    public int getPickingFragile() {
        return PickingFragile;
    }

    public void setPickingFragile(int pickingFragile) {
        PickingFragile = pickingFragile;
    }

    public String getPickingRemark() {
        return PickingRemark;
    }

    public void setPickingRemark(String pickingRemark) {
        PickingRemark = pickingRemark;
    }
}
